package extra;

public class Gauntlet {
public static void main(String[] args) {
	System.out.println("Single Test One");
	for(int i=0; i<=100; i++) {
		System.out.println(i);
		
	}
	System.out.println("Single Test Two");
	for (int i=100; i >=0 ;i-=1) {
		System.out.println(i);
		
	}
	System.out.println("Single Test Three");
	for(int i=2; i<=100; i+=2) {
		System.out.println(i);
		
	}
	System.out.println("Single Test Four");
	for(int i=1; i<=99; i+=2) {
		System.out.println(i);
		
	}
}
}
